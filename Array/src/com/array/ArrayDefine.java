package com.array;
// To print the Array Limit
public class ArrayDefine {
	public static void main(String[] args) {
		int ar[] = {11,12,13,14,15,16,17};  // Array Declaration and Initialization
		for(int i=0;i<=ar.length-1;i++) {   // for traversing array we go for for loop
			System.out.println(ar[i]);
		}

	}
}
